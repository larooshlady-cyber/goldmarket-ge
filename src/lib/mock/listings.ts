export const getListingById = (id: string): Listing | undefined => {
  return listings.find((listing) => listing.id === id);
};

export const getListingBySlug = (slug: string): Listing | undefined => {
  return listings.find((listing) => listing.slug === slug);
};

export const getListingsBySellerId = (sellerId: string): Listing[] => {
  return listings.filter((listing) => listing.sellerId === sellerId);
};

export const getListingsByCategory = (categoryId: string): Listing[] => {
  if (categoryId === 'all') return listings;
  return listings.filter((listing) => listing.categoryId === categoryId);
};

export const getListingsByVipLevel = (vipLevel: string): Listing[] => {
  return listings.filter((listing) => listing.vipLevel === vipLevel);
};

export const getSuperVipListings = (): Listing[] => getListingsByVipLevel('super_vip');
export const getVipPlusListings = (): Listing[] => getListingsByVipLevel('vip_plus');
export const getVipListings = (): Listing[] => getListingsByVipLevel('vip');
